// Simple examples of strings (text) in JavaScript
// You can copy and paste to practice

"This is a string";
"12"; // string with number
12;   // number

'This is also a string'; // string with single quotes
'This is a string';  // fixed quote

'<a href="">';  // example with HTML
'<a href="http://www.example.com">';  // example with HTML link

"This is Luis's favorite string";  // Luis's string
"This is Luis's \"favorite\" string";  // escape double quotes
"This is Luis's 'favorite' string";  // double and single quotes mixed

// Multi-line string (string with many lines)
"This is \
Luis's Favorite \
String EVER";

console.log("This is a string");
console.log("12"); // string with number
console.log(12);   // number (not string)

console.log('This is also a string'); // string with single quotes
console.log('This is a string');  // fixed quote

console.log('<a href="">');  // example with empty HTML link
console.log('<a href="http://www.example.com">');  // example with HTML link

console.log("This is Luis's favorite string");  // Luis's string
console.log("This is Luis's \"favorite\" string");  // escape double quotes inside
console.log('This is Luis\'s "favorite" string');  // another way with single quotes outside

// Multi-line string (string with many lines)
console.log("This is \
Luis's Favorite \
String EVER");

